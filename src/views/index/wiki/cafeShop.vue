                            埃及的咖啡馆俗称为 ' ahwah /ʔhwa/ ，这是 قَهْوةqahwah（字面意思为“咖啡”）的方言发音（
                            另见阿拉伯语音学#地方变体）。 阿瓦中常见 ' 的还有茶（shāy）和草本茶
                            ，尤其是非常受欢迎的扶桑花混合茶（埃及阿拉伯语 ：karkadeh 或 ennab）。第一家 '
                            阿瓦餐厅大约在 1850年代开业，最初主要由年长者光顾，年轻人虽然常来，但并不总是点餐。到 1920
                            年代，他们与俱乐部（开罗）、 布尔萨 （ 亚历山大） 和乡村旅馆（gharza）有关联。20世纪初，

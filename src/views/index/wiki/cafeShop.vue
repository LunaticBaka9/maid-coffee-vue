                        <h4>意式咖啡吧</h4>
                        <p>
                            意式咖啡吧是一种专门供应意式浓缩咖啡饮品的咖啡馆。它起源于意大利，如今已发展成为遍布全球的各种形式的咖啡馆。国际知名的典型例子包括总部位于美国西雅图的星巴克咖啡和总部位于英国劳德沃特的
                            Costa 咖啡
                            （分别是全球第一和第二大咖啡连锁店），但实际上，意式咖啡吧在世界各地都以某种形式存在。
                        </p>
                        <p>
                            意式咖啡吧通常以一个长吧台为中心，吧台上摆放着一台高出水量的意式咖啡机
                            （通常是全自动或半自动泵式咖啡机，偶尔也有手动杠杆活塞式咖啡机），旁边还有一个展示柜，里面摆放着糕点，偶尔也会有三明治等咸味小吃。在传统的意大利咖啡吧，顾客要么在吧台点单后站着享用饮品，要么如果想坐下来由服务员服务，通常需要支付更高的价格。有些咖啡吧还会对在户外餐桌享用的饮品收取额外费用。而在其他国家，尤其是美国，咖啡吧会免费提供供顾客放松和工作的座位区。一些意式咖啡吧还会出售咖啡用具、糖果，甚至音乐。北美的意式咖啡吧也率先广泛普及了公共
                            Wi-Fi 接入点，为在店内使用笔记本电脑工作的顾客提供互联网服务。
                        </p>
                        <p>
                            典型的意式咖啡吧提供的点心通常深受意大利风味的影响； 意式脆饼 、
                            卡诺里卷和薄脆饼是搭配拿铁或卡布奇诺的常见传统点心。一些高档咖啡吧甚至提供格拉巴酒和桑布卡酒等酒精饮品。然而，典型的糕点并不总是完全意大利风味，常见的还有司康饼
                            、 松饼 、 羊角面包 ，甚至甜甜圈
                            。通常也会提供种类繁多的茶饮，而北美的咖啡吧文化也促成了印度香料茶玛萨拉奶茶的流行。冰饮在一些国家也很受欢迎，包括冰茶、冰咖啡以及星巴克星冰乐等混合饮品。
                        </p>

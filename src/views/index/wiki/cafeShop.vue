                            tiàm）。菜单通常提供简单的菜肴：以鸡蛋 、 吐司和椰子酱为基础的各种食物，以及咖啡、茶和
                            Milo，一种在东南亚和澳大拉西亚，尤其是新加坡和马来西亚极受欢迎的麦芽巧克力饮品。

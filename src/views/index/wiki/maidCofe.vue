                        <h3>创立</h3>
                        <p>
                            随着女仆咖啡厅的日益普及，也让一些服务员成为非常受欢迎的人物，犹如偶像一般的存在。有些除了在女仆咖啡厅拥有工作外，也会在外进行一些广告代言或者表演的工作。
                            第一批由女仆所组成的偶像团队，于2005年由“@home”成立。而在其他情况下，也有偶像反而是在女仆咖啡厅打工工作。如在“@home”这家女仆咖啡厅中，就共有16位女仆同时拥有了女服务生和偶像的双重角色定位。2005年被认为是女仆咖啡厅的巅峰。
                            但不久之后，由于有许多经营者仍对女仆咖啡厅没有一个明确的概念，或者因当地民众反应并不热烈的缘故，而出现有部分店家倒闭的情况。如2003年2月14日“Cafe
                            Mai:lish”于武藏野市的吉祥寺开设了第二家分店，却仍于2004年2月15日宣布停止营运。
                            时间的推移也让诞生地秋叶原的女仆咖啡厅数量快速增加了。到了2008年时，游客可很容易地在秋叶原附近找到近20家的咖啡厅，这还不包括其他类型的店家，如果包含有穿着女仆服的女服务生在内的店面则会更多。随着女仆咖啡厅是如此受到欢迎，不同类型的店家也开始在原本的服务上加入女仆地成分，如酒吧、按摩治疗师等等。女仆咖啡厅也成为秋叶原的特有现象，在秋叶原预估有五十多家各类近似的店家。
                            不过虽然女仆咖啡厅可说是秋叶原的特有文化，一些女仆咖啡厅也已经打开日本和海外市场。包括沿着东京首都圈的池袋、涩谷、新宿、中野、横滨等都开始有女仆咖啡厅的出现。其中在池袋车站与太阳城前的“乙女路”，由于其以腐女为主的客户群跟以御宅族为主的秋叶原不同，导致该处的角色扮演餐厅与秋叶原有着不同的变化，也就是2005年时出现的执事咖啡厅。到了2006年为止，该地区已经有近8家类似的店开设于此。
                            除了东京外，为了吸引居住在日本各地的御宅族，各地区皆有女仆咖啡厅的开设。如在2002年9月13日，名古屋市的大须开设了当地第一家女仆咖啡厅“M's
                            Melody”；2003年2月17日时，札幌市开设了当地第一家女仆咖啡厅“Cafe
                            Primevere”；2003年2月17日时，神户市开设了当地第一家女仆咖啡厅“KANON”。2004年10月16日时，大阪市开设了当地第一家女仆咖啡厅“CCOちゃ”，2005年9月18日时，仙台市开设了当地第一家女仆咖啡厅“fairy
                            tale”。自此，从北海道到九州等较为偏远的日本地方也都有开设女仆咖啡厅。
                            这股风潮也在2006年加拿大多伦多开设第一家海外的女仆咖啡厅后，迅速扩展至其他海外国家。到了今日，包括北京、上海、广州、韩国首尔、台湾台北、台中、台南、高雄、泰国曼谷、香港、新加坡、墨西哥、美国和法国巴黎等地都有女仆咖啡厅的出现。

                        <h3>创立</h3>
                        <p>
                            1998年8月，第一家可体验女仆服务形式的咖啡厅店面是为了在东京角色展推广成人游戏《欢迎来到Pia
                            Carrot!!》而设置的。Broccoli借着一群女服务生穿着游戏中的制服，为前来参观的观众提供食物和饮料的销售。这一次促销战略的成功，也代表着第一家角色扮演餐厅的出现，也让之后许多游戏或动画以类似的方式进行推广。
                            如在2000年2月25日时，Broccoli为宣传《银河天使》而另行设计展示了专属的角色精品店“GAMERS”，并以女仆的概念为其设计了象征吉祥物Di
                            Gi Charat。而在2003年4月6日，为宣传《朝雾的巫女》也有相关主题的咖啡厅出现。
                            第一家被认定真正从创店开始便以女仆为导向的咖啡厅，则为2001年3月30日开设的“Cure
                            Maid”。在当时，她们身上的女仆装是由cosplay服装厂商“Cospa”承包制成。在一开始的时候，“Cure
                            Maid”的目标便是在类似19世纪的装潢下，提供一个供客人休息的宜人且私密的环境，并尝试建立一个主仆关系的女仆咖啡厅。在这里，女服务生会穿着维多利亚时代的女仆服，给客户拥有了一种礼貌、服从的印象。
                            “Cure
                            Maid”咖啡厅除了偶尔作为一些演讲或活动地点外，会与Cospa合作进行一些动画游戏的Cosplay展示。此外，在每个礼拜六，“Cure
                            Maid”会邀请学生于此演奏小提琴、长笛或竖琴等乐器。虽然直到今日，许多女仆咖啡厅已经改变当初原本的初衷，大多是以“萌”作为自身的主要目标；但“Cure
                            Maid”仍坚持保留其自开业以来的理念，这些年间并没有变化甚大。虽然与现今的有些不同，但“Cure
                            Maid”由于在意义上为首家将女仆装列为女服务生制服的咖啡厅，因此被大多数人认定为第一家真正的“女仆咖啡厅”。
                            而最早以萌作为特色的女仆咖啡厅，则是同样位在秋叶原的“Cafe
                            Mai:lish”。该家咖啡厅是源自作为个人电脑零售商店T-ZONE下辖的咖啡厅、于2002年7月19日创立的女仆咖啡厅─“Mary's”，到了同年10月1日时改名为今“Cafe
                            Mai:lish”的名称。该餐厅大量邀集与日本动画、游戏相关的活动，并也会让女仆咖啡厅内的女服务生与顾客一同参与Cosplay活动。同时“Cafe
                            Mai:lish”也是第一家禁止拍摄女服务生的咖啡厅，不过仍允许顾客拍摄桌上的饮品与食物。
                            随着秋叶原地区不断举办与女仆咖啡厅相关的活动，使得其也开始被大众媒体播导，也让社会大众得知这一新兴产业。继“Cure
                            Maid”和“Cafe
                            Mai:lish”等于秋叶原快速发展的女仆咖啡厅之后，各家女仆咖啡厅也于日本各处开始纷纷成立。
                            基本上，女仆咖啡厅为顾客所提供的基本服务与传统咖啡店的服务是一致的，但通常还会加上与女仆的各式互动，如拍照、共同玩电玩游戏或者一同欣赏日本动画等。
                            随着女仆咖啡厅的成功，这类借由Cosplay来吸引客户的店面也纷纷出现，俨然成为一种次文化。包括酒吧、居酒屋、餐厅等一些店家都开始有扮成女仆的女服务生出现，而这些店又大多以御宅族为主要的客户对象。
                            而在一些日本漫画、动画、游戏、电视节目中，女仆咖啡厅的形象也更加的鲜明。
